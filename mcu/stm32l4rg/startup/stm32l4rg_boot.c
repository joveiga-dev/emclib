#define STACK_TOP (SRAM_END)

/* Symbols from linker script */
extern uint32_t __estack;

extern uint32_t __load_data;

extern uint32_t __start_data;
extern uint32_t __end_data;

extern uint32_t __start_bss;
extern uint32_t __end_bss;

/* Forward declarations */
extern int main(void);
void Reset_Handler(void);
void Default_Handler(void);

typedef void (*int_handler_t)(void);

/**
 * Zero static variables with zero or no initial value
 */
void zero_bss(void)
{
    uint32_t *p_dst = &__start_bss;
    while (p_dst < &__end_bss)
    {
        *p_dst++ = 0;
    }
}

/* Copy default data to .data section */
void copy_data(void)
{
    uint32_t *p_dst = &__start_data;
    uint32_t *p_src = &__load_data;
    while(p_dst < &__end_data)
    {
        *p_dst++ = *p_src++;
    }
}

/**
 * Reset Handler code. This will be executed when the MCU comes out of reset
 */
void Reset_Handler(void)
{
    copy_data();
    zero_bss();
    // call system init??
    main(); 
    while (1)
        (void)0;
}

void Default_Handler(void)
{
    while (1)
        (void)0;
}

